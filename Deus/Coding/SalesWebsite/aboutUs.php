	The idea for Deus was conceived shortly after Andrew had been invited by Miro to attend the 2015 Crisis Committee at Cambridge University International MUN (CUIMUN). Convinced that Google Forms weren’t the best method for running a Crisis Andrew pitched his idea for a better system to Miro and they buy the hugely creative domain, “MUNCrisis.com”.<br><br>

After attending several more conferences together and finding out from people what they would like to see, Andrew began writing some solution on his raspberry pi (because he’s so edgy). Although he got distracted by inventing a card game Andrew finally finds some time over the summer to teach himself PHP and creates the basic version of Deus. Throughout long skype sessions and trial runs at York and Manchester Andrew and Miro work out some kinks and refine the product.<br><br>

When Miro managed to get a spot on the Oxford international MUN’s 2016 Crisis Team he took Deus with him and introduced it to a large international audience. Since then demand for Deus has been increasing exponentially and we’ve received requests from as far as Venezuela and Delhi.  <br><br>

To date Deus has been used by over 15 conferences including some of Europe’s largest such as OxIMUN, HamMUN, LIMUN and PIMUN. Thanks to early adopters such ManMUN, OxIMUN, HamMUN and the feedback we’ve received from individuals and conferences (particularly UBIMUN) Deus has improved continuously! We are looking forward to continue to make professionally run Crisis Committees easier and more accessible.
